declare global {
  interface Array<T> {
    snail(rowsCount: number, colsCount: number): number[][];
  }
}

Array.prototype.snail = function (
  rowsCount: number,
  colsCount: number
): number[][] {
  if (rowsCount * colsCount !== this.length) return [];

  const res: number[][] = [];
  for (let m = 0; m < rowsCount * colsCount; m++) {
    const counter = Math.ceil((m + 1) / rowsCount);

    if (res[m % rowsCount] === undefined) {
      let row: number[] = [];
      row.push(this[m]);
      res.push(row);
    } else {
      let index = m % rowsCount;
      if (counter % 2 === 0) index = rowsCount - 1 - index;
      res[index].push(this[m]);
    }
  }

  return res;
};

