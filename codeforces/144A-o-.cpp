#include <iostream>
#include <vector>

int main() {
    int seconds{}, size, min{}, max, min_index, max_index;
    std::vector <int> line;

    std::cin >> size;
    min_index = max_index = size;
    for (int i = 0; i < size; i++) {
        int height;
        std::cin >> height;
        line.push_back(height);
        
        if (height > max) {
            max = height;
            max_index = i;
        }
        if (height <= min || i == 0) {
            min = height;
            min_index = i;
        }
    }

    seconds = max_index < min_index
        ? max_index + (size - min_index - 1)
        : max_index + (size - min_index - 2);

    std::cout << seconds << std::endl;

    return 0;
}