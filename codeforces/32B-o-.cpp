#include <iostream>
#include <string>

int main() {
    int count{};
    std::string code;
    std::getline(std::cin, code);

    for (int i = 0; i < code.length(); i++) {
        if(!count && code[i] == '.')
            std::cout << 0;
        else if (count) {
            code[i] == '.'
             ? std::cout << 1
             : std::cout << 2;
            count = 0;
        }
        else count++;
    }
    std::cout << "\n";

    return 0;
}