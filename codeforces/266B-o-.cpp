#include <iostream>
#include <vector>

int main() {
    int size{}, time{};
    std::cin >> size >> time;
    std::vector <char> queue;
    
    for (int i = 0; i < size; i++) {
        char inp;
        std::cin >> inp;
        queue.push_back(inp);
    }

    for (int i = 0; i < time; i++) {
        for (int i = 0; i < size-1; i++) {
            if(queue.at(i) == 'B' && queue.at(i+1) == 'G') {
                queue[i] = 'G';
                queue[i+1] = 'B';
                i++;
            }
        }
    }

    for (char c : queue) {
        std::cout << c;
    }
    std::cout << "\n";

    return 0;
}