#include <iostream>
#include <vector>

using namespace std;

int main() {
    int v = 0, p = 0;
    int n, m, b;

    cin >> n;

    vector<int> arr{};
    for (int i = 0; i < n; i++) {
        int ele;
        cin >> ele;
        arr.push_back(ele);
    }

    cin >> m;
    for (int i = 0; i < m; i++) {
        int b;
        cin >> b;
        
        for (int j = 0; j < n; j++) {
            if (arr[j] == b) {
                v += j + 1;
                p += n - j;
                break;
            }
        }
    }

    cout << v << " " << p << endl;

    return 0;
}