use std::io;

fn main() {
    let mut length = String::new();
    let mut row = String::new();
    io::stdin()
        .read_line(&mut length)
        .expect("failed to read the line");

    io::stdin()
        .read_line(&mut row)
        .expect("failed to read the line");

    let mut moves = 0;
    let mut prev_c = '\0';
    for c in row.trim().chars() {
        if c == prev_c {
            moves += 1;
        }
        prev_c = c;
    }

    println!("{}", moves);
}
