use std::io;

fn main() {
    let mut input = String::new();
    io::stdin()
        .read_line(&mut input)
        .expect("failed to read the input!");

    let lucky_digits = input
        .trim()
        .chars()
        .filter(|c| *c == '4' || *c == '7')
        .count();

    let is_lucky = lucky_digits
        .to_string()
        .chars()
        .all(|c| c == '4' || c == '7');

    if is_lucky {
        println!("YES");
    } else {
        println!("NO");
    }
}
