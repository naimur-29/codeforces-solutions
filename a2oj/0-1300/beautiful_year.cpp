#include <iostream>
#include <string>

using namespace std;

int main() {
    int year;
    cin >> year;

    bool is_distinct = false;
    while(!is_distinct) {
        year++;
        string str = to_string(year);
        is_distinct = true;
        for (int i = 0; i < str.length(); i++) {
            for (int j = 0; j < str.length(); j++) {
                if (is_distinct && j != i && str[i] == str[j]) {
                    is_distinct = false;
                }
            }
        }
    }
    cout << year << endl;

    return 0;
}