#include <iostream>
#include <vector>

using namespace std;

int main() {
    int total, iter;
    cin >> total >> iter;

    vector<char> queue(total);

    for (char& person : queue) {
        cin >> person;
    }

    for (int i = 0; i < iter; i++) {
        for (int j = 0; j < total - 1; ++j) {
            if (queue[j] == 'B' && queue[j + 1] == 'G') {
                std::swap(queue[j], queue[j+1]);
                ++j;
            }
        }
    }

    for (char person : queue) {
        cout << person;
    }
    cout << "\n";

    return 0;
} 