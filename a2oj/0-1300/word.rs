use std::io;

fn main() {
    let mut str = String::new();
    io::stdin()
        .read_line(&mut str)
        .expect("Failed to read input");

    let mut up = 0;
    let mut low = 0;
    for c in str.trim_end().chars() {
        if c.is_ascii_uppercase() {
            up += 1;
        } else {
            low += 1;
        }
    }

    if up > low {
        str.make_ascii_uppercase();
    } else {
        str.make_ascii_lowercase();
    }

    println!("{}", str.trim_end());
}
