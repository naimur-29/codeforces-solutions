use std::io;

fn main() {
    let mut str = String::new();
    io::stdin()
        .read_line(&mut str)
        .expect("failed to read the input");

    let mut s = str.trim_end().to_string();
    if let Some(c) = s.chars().next() {
        if c.is_ascii_uppercase() {
            println!("{}", s);
            return;
        }
        s.replace_range(..1, &c.to_uppercase().to_string());
    }
    println!("{}", s);
}
