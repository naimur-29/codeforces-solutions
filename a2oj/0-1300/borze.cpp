#include <iostream>
#include <string>

using namespace std;

int main() {
    string str, out = "";
    getline(cin, str);

    char prev_code;
    
    for(char code: str) {
        if (code == '.') {
            if (prev_code == '-') {
                out += "1";
            } else {
                out += "0";
            }
            prev_code = ' ';
        } else {
            if (prev_code == '-') {
                out += "2";
                prev_code = ' ';
            } else {
                prev_code = code;
            }
        }
    }
    cout << out << "\n";

    return 0;
}