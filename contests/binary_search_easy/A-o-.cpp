#include <iostream>
#include <vector>

int FindElementIndex(std::vector <int> arr, int arr_size, int element) {
    bool isFound = false;
    int left = 0, right = arr_size-1, mid = (left + right)/2;
    while(!isFound && left <= right) {
        if (arr[mid] == element) isFound = true;
        else if (arr[mid] > element) {
            right = mid - 1;
            mid = (left + right)/2;
        }
        else {
            left = mid + 1;
            mid = (left + right)/2;
        }
    }

    return isFound ? mid : -1;
} 

int main() {
    std::vector <int> arr;
    int size, key, index{};
    std::cin >> size;

    for (int i = 0; i < size; i++) {
        int num;
        std::cin >> num;
        arr.push_back(num);
    }
    std::cin >> key;

    if (!size)
        std::cout << -1 << std::endl;
    else
        std::cout << FindElementIndex(arr, size, key) << std::endl;

    return 0;
}