#include <cstdio> 
#include<iostream> 
#include <cmath> 

using namespace std;

int arr[100009];

int main(){
    int n, t, pos = 0, sum = 0, k = 0;
    cin >> n >> t;

    for(int i=0; i < n; i++) {
        cin >> arr[i];
    }

    for(int i = 0; i < n; i++) {
        sum += arr[i];

        while(sum > t) {
            sum -= arr[pos];
            pos++;
        }
        if(k <= i - pos + 1) {
            k = i - pos + 1;
        }

    }
    cout << k << endl;

    return 0;
}