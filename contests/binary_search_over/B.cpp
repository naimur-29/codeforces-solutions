#include <iostream>

using namespace std;

int main() {
	long long n, f, left = 1, mid;

    while(cin >> n) {
		cin >> f;
    	int cnt = 0;
        while(left < n) {
            mid=(n + left) / 2;
            cnt++;

            if(mid < f) {
                left = mid + 1;
			} else {
                n = mid;
			}
        }
        cout << cnt << endl;
    }
    return 0;
}