#include <iostream>
#include <algorithm>

using namespace std;

int main() {
    long long t;
    cin >> t;

    while (t--) {
		int stalls, cows, arr[100001];
		cin >> stalls >> cows;
		for (int i = 0; i < stalls; i++) {
			cin >> arr[i];
        }
		sort(arr, arr + stalls);

		int left = arr[0], right = arr[stalls - 1], mid = (right + left) / 2, gap = 0;
		while (left <= right) {
			int count = 1, current = 0;
			for (int i = 1; i < stalls && count < cows; i++) {
				mid = (right + left) / 2;
				if (arr[i] - arr[current] >= mid) {
					current = i;
					count++;
				}
			}
			if (count == cows) {
				left = mid + 1;
				gap = mid;
			}
			else {
				right = mid - 1;
            }
		}

        cout << gap << endl;
    }
    return 0;
}