#include<iostream>

using namespace std;
 
int sum[100009];
 
int main() {
	int m, n, temp;
	scanf("%d", &n);
	scanf("%d", &temp);

	sum[0] = temp;
	for(int i = 1; i < n; ++i) {
		scanf("%d", &temp);
		sum[i]=sum[i-1]+temp;
	}

	scanf("%d", &m);
	while(m--) {
		scanf("%d", &temp);
		int t = lower_bound(sum, sum + n, temp) - sum;

		printf("%d\n", t + 1);
	}

	return 0;
}