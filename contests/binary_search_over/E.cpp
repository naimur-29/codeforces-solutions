#include <iostream>

using namespace std;

long long int binary_search (long long int arr[], long long int low, long long int high, long long int x){
    long long int res = -1;

    while(low <= high) {
        long long int mid = (high + low) >> 1;

        if(arr[mid] == x) {
            res = mid;
            high = mid - 1;
        } else if (arr[mid] < x) {
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }

    return res;
}

int main(){
    long long int n, q, k;
    cin >> n >> q;

    long long int arr[100009];
    for(long long int i = 0; i < n; i++) {
        scanf("%lld", &arr[i]);
    }

    while(q--) {
        scanf("%lld", &k);
	    printf("%lld\n", binary_search(arr, 0, n - 1, k));
    }

    return 0;
}