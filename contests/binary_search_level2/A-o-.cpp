#include <iostream>

int main() {
    int t, n, size{};
    std::cin >> t;

    for (int i = 0; i < t; i++) {
        std::cin >> n;
        size = 1;
        n -= 1;

        do {
            n -= size + 1;
            if (n < 0) break;
            size++;
        } while(n);

        std::cout << size << std::endl;
    }

    return 0;
}