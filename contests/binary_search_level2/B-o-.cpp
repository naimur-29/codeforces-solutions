#include <iostream>
#include <cmath>

int Teacher(long long int num, long long int ending_index) {
    long long int count{}, starting_index{1}, mid_index{};

    while (starting_index < ending_index) {
        mid_index = (starting_index + ending_index) / 2;
        count++;

        if (num > mid_index) {
            starting_index = mid_index + 1;
        }
        else {
            ending_index = mid_index;
        }
    }

    return count;
}

int main() {
    long long int n, x;
    std::cin >> n >> x;

    std::cout << Teacher(x, n) << std::endl;

    return 0;
}